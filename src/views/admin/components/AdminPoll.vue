<template>
  <div class="admin-poll">
    <h2 class="subtitle">
      Admin Poll
    </h2>
    <div class="columns">
      <div class="column is-half">
        <div class="panel">
          <p class="panel-heading">
            Create a Poll
          </p>
          <div class="panel-block">
            <p class="control">
              <b-input
                v-model="options.question"
                style="margin-bottom: 10px"
                placeholder="Question"
                type="text"
                class=""
              />
              <b-field grouped>
                <b-input
                  v-model="currentAnswer"
                  placeholder="Answer"
                  type="text"
                  expanded
                  @keyup.enter.native="addAnswer"
                />
                <b-button
                  class="button"
                  @click="addAnswer"
                >
                  Add Answer
                </b-button>
              </b-field>
              <b-field class="answer_list">
                <ul>
                  <template
                    v-for="answer in options.answers"
                  >
                    <li
                      :key="answer.text"
                      class="answer"
                      @click="removeAnswer(answer)"
                    >
                      â€¢ {{ answer.text }}
                    </li>
                  </template>
                </ul>
              </b-field>
              <b-field
                v-if="options.question && options.answers.length > 0"
              >
                <div class="endDate">
                  End Date:
                </div>
                <b-datepicker
                  v-model="endDate"
                  placeholder="Type or select a date..."
                  icon="calendar"
                  editable
                />
                <b-timepicker
                  v-model="endDate"
                  placeholder="Type or select a time..."
                  icon="clock"
                  hour-format="12"
                />
                <b-button
                  class="button"
                  type="is-primary"
                  @click="createPoll"
                >
                  Submit Poll
                </b-button>
              </b-field>
            </p>
          </div>
        </div>
      </div>
      <div class="column is-half">
        <Poll
          :options.sync="options"
        />
      </div>
    </div>
    <div class="buttons">
      <b-button
        type="is-danger"
        icon-left="trash"
        @click="deleteConfirm=true"
      >
        Delete expired polls
      </b-button>
      <div v-if="deleteConfirm">
        <b-field grouped>
          <div style="font-size: 1.5em; padding-right: 0.5em;">
            Are you sure?
          </div>
          <b-button
            type="is-danger"
            title="There's no going back"
            @click="deleteAllPolls"
          >
            Yes
          </b-button>
          <b-button
            type="is-success"
            @click="deleteConfirm=false"
          >
            No
          </b-button>
        </b-field>
      </div>
    </div>
    <div style="float: left; width: 50%;">
      <AdminPollViewer />
    </div>
  </div>
</template>

<script>

import Poll from '@/views/polls/Poll'
import AdminPollViewer from '@/views/polls/AdminPollViewer'

export default {
  name: 'AdminFun',
  components: {
    Poll,
    AdminPollViewer
  },
  data () {
    return {
      options: {
        question: '',
        answers: [],
        showResults: false
      },
      currentAnswer: '',
      currentID: 0,
      endDate: new Date(),
      deleteConfirm: false
    }
  },
  methods: {
    addAnswer () {
      if (!this.currentAnswer) return // do nothing if input is empty
      let answer = { value: this.currentID++, text: this.currentAnswer, votes: 0 }

      this.options.answers.push(answer)

      this.currentAnswer = '' // clear answer input box
    },
    removeAnswer (answer) {
      this.options.answers = this.options.answers.filter(e => e.text !== answer.text)
    },
    async createPoll () {
      // submit poll to KOA server
      let request
      try {
        request = await this.$http.post('/polls', { options: this.options, endDate: this.endDate })
      } catch (e) {
        console.error(e)
      }
    },
    async deleteAllPolls () {
      this.deleteConfirm = false
      let request
      try {
        request = await this.$http.delete('/polls')
      } catch (e) {
        console.error(e)
      }
      alert('You deleted ' + request.data + ' poll(s)')
    }
  }
}
</script>

